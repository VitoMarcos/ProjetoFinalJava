package Classes.Evento;

public class TimeA {

    private String nome;
    private double odd;

    public TimeA(String nome, double odd) {
        this.nome = nome;
        this.odd = odd;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public double getOdd() {
        return odd;
    }

    public void setOdd(double odd) {
        this.odd = odd;
    }

}